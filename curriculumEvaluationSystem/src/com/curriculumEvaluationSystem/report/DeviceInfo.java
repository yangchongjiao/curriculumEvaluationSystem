package com.curriculumEvaluationSystem.report;

public class DeviceInfo
{
	private String imei;

	private String imsi;

	private String language;

	private String model;

	private String isEmulator;

	private String serialNo;

	private String manufacturer;

	private String sdkLevel;

	private String screenHeight;

	private String screenWidth;

	private String deviceName;

	public String getImei() {
		return imei;
	}

	public void setImei(String imei) {
		this.imei = imei;
	}

	public String getImsi() {
		return imsi;
	}

	public void setImsi(String imsi) {
		this.imsi = imsi;
	}

	public String getLanguage() {
		return language;
	}

	public void setLanguage(String language) {
		this.language = language;
	}

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public String getIsEmulator() {
		return isEmulator;
	}

	public void setIsEmulator(String isEmulator) {
		this.isEmulator = isEmulator;
	}

	public String getSerialNo() {
		return serialNo;
	}

	public void setSerialNo(String serialNo) {
		this.serialNo = serialNo;
	}

	public String getManufacturer() {
		return manufacturer;
	}

	public void setManufacturer(String manufacturer) {
		this.manufacturer = manufacturer;
	}

	public String getSdkLevel() {
		return sdkLevel;
	}

	public void setSdkLevel(String sdkLevel) {
		this.sdkLevel = sdkLevel;
	}

	public String getScreenHeight() {
		return screenHeight;
	}

	public void setScreenHeight(String screenHeight) {
		this.screenHeight = screenHeight;
	}

	public String getScreenWidth() {
		return screenWidth;
	}

	public void setScreenWidth(String screenWidth) {
		this.screenWidth = screenWidth;
	}

	public String getDeviceName() {
		return deviceName;
	}

	public void setDeviceName(String deviceName) {
		this.deviceName = deviceName;
	}

	

}
